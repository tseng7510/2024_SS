  <script>
    let x;
    let check;
    let windowWidth = window.innerWidth;
    let scroll = document.querySelector(".block6");
    let listBox = document.querySelector(".block6 .picBox");
    let body = document.querySelector("body");
    let outWidth = scroll.offsetWidth;

    window.addEventListener("resize", () => {
      windowWidth = window.innerWidth;
      outWidth = scroll.offsetWidth;
    });
    scroll.addEventListener("mousedown", function(event) {
      let rect = scroll.getBoundingClientRect();
      x = event.clientX;
      check = true;
    });

    document.addEventListener("mouseup", function(event) {
      check = false;
      body.classList.remove("active");
      listBox.classList.remove("active");
    });



    let mouseMove = () => {
      if (check) {
        body.classList.add("active");
        listBox.classList.add("active");
        if (check) {
          let moveX = event.clientX - x;
          moveX > outWidth ?
            (moveX = outWidth) :
            moveX < outWidth * -1 ?
            (moveX = 0) :
            (moveX = event.clientX - x);
          if (moveX > 0) {
            listBox.scrollLeft -= Math.abs(moveX) / 20;
          } else {
            listBox.scrollLeft += Math.abs(moveX) / 20;
          }
        }
      }
    };

    window.addEventListener("mousemove", mouseMove);

    scroll.addEventListener("mouseleave", () => {
      body.classList.remove("active");
      listBox.classList.remove("active");
    });
    let allLeft = [];
  </script>
